    private boolean checkValidateDonHang() {
        maSanPham = txt_maSanPham.getText().trim().matches("[0-9]+") ? Integer.valueOf(txt_maSanPham.getText()) : -1;
        soLuong = txt_soluong.getText().trim().matches("[0-9]+") ? Integer.valueOf(txt_soluong.getText()) : -1;

        if (maSanPham == -1) {
            JOptionPane.showMessageDialog(this, "Chưa chọn SP");
            return false;
        }
        if (soLuong < 1) {
            JOptionPane.showMessageDialog(this, "Số lượng không hợp lệ");
            txt_soluong.requestFocus();
            return false;
        }

        sanPham = sanPhamDAO.getSanPhamById(maSanPham);
        int soluong_ = soLuong;
        for (HoaDonChiTiet hoaDonChiTiet : listHoaDonChiTiet) {
            if (hoaDonChiTiet.getSanPham().getMaSanPham() == maSanPham) {
                soluong_ += hoaDonChiTiet.getSoLuong();
                break;
            }
        }
        if (soluong_ > sanPham.getTonKho()) {
            JOptionPane.showMessageDialog(this, "Vượt quá số lượng tồn kho của Sản phẩm");
            return false;
        }

        return true;
    }

    private boolean checkValidateHoaDon() {
        taiKhoan = TrangChu.taiKhoan;
        if (maKhachHang == -1 || txt_tenKhachHang.getText().trim().isEmpty()) {
            JOptionPane.showMessageDialog(this, "Chưa chọn Khách Hàng");
            return false;
        }
        if (listHoaDonChiTiet.isEmpty() || modelHoaDonChiTiet.getRowCount() == 0) {
            JOptionPane.showMessageDialog(this, "Chưa chọn SP");
            return false;
        }

        ngayLapHoaDon = format.format(new java.util.Date());
        ghiChu = txt_ghiChu.getText();
        return true;
    }

    private void loadModelHoaDonChiTiet() {
        int stt = 1;
        tongTien = 0;
        modelHoaDonChiTiet.setRowCount(0);
        for (HoaDonChiTiet hoaDonChiTiet : listHoaDonChiTiet) {
            Vector row = new Vector();
            row.add(stt++);
            row.add(hoaDonChiTiet.getSanPham().getMaSanPham());
            row.add(hoaDonChiTiet.getSanPham().getTenSanPham());
            row.add(hoaDonChiTiet.getSanPham().getTonKho());
            row.add(hoaDonChiTiet.getSoLuong());
            row.add(format(hoaDonChiTiet.getSanPham().getGiaBan()));
            int thanhTien = hoaDonChiTiet.getSanPham().getGiaBan() * hoaDonChiTiet.getSoLuong();
            row.add(format(thanhTien));
            modelHoaDonChiTiet.addRow(row);
            tongTien += thanhTien;
        }
        txt_TongTien.setText(format(tongTien));
    }

