    private void themDonHang() {
        if (checkValidateDonHang()) {
            try {
                addListHoaDonChiTiet();
                loadModelHoaDonChiTiet();

                txt_maSanPham.setText("");
                txt_tenSanPham.setText("");
                txt_loaiSanPham.setText("");
                txt_hangSanXuat.setText("");
                txt_soluong.setText("");

                tbl_DonHang.setRowSelectionInterval(modelHoaDonChiTiet.getRowCount() - 1, modelHoaDonChiTiet.getRowCount() - 1);

            } catch (IllegalArgumentException iae) {
            } catch (Exception e) {
                JOptionPane.showMessageDialog(this, "Lỗi thêm đơn hàng");
                System.out.println(e);
            }
        }
    }

    private void addListHoaDonChiTiet() {
        boolean isDuplicate = false;
        for (HoaDonChiTiet hoaDonChiTiet : listHoaDonChiTiet) {
            if (hoaDonChiTiet.getSanPham().getMaSanPham() == maSanPham) {
                isDuplicate = true;
                hoaDonChiTiet.setSoLuong(hoaDonChiTiet.getSoLuong() + soLuong);
                break;
            }
        }
        if (!isDuplicate) {
            HoaDonChiTiet hoaDonChiTiet = new HoaDonChiTiet();
            hoaDonChiTiet.setSanPham(sanPham);
            hoaDonChiTiet.setSoLuong(soLuong);
            listHoaDonChiTiet.add(hoaDonChiTiet);
        }
    }

